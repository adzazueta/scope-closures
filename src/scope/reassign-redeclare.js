// VAR
var firstName; // Declare
firstName = "Alexis"; // Assign
console.log(firstName); // "Alexis"

var lastName = "Zazueta"; // Declare and assign
lastName = "Ortega"; // Reassign
console.log(lastName); // "Ortega"

var secondName = "Daniel"; // Declare and assign
var secondName = "Alexis"; // Redeclare and reassign
console.log(secondName); // "Abi"

// LET
let fruit = "Apple"; // Declare and assign
fruit = "Kiwi"; // Reassign
console.log(fruit); // "Kiwi"

// CONST
const animal = "Dog"; // Declare and assign
console.log(animal); // "Dog"
